#include <stdio.h>
int main(void) {
    int number;
    //initialise counter to 1
    int initial = 1;
    
    //input number.
    printf("Enter number: ");
    scanf("%d", &number);
    
    // loop until not >=number.
    while ( initial < number) {
    
        if (initial % 3 == 0 || initial % 5 == 0) { 
            //print number
            printf("%d\n", initial);
        }
        //increase number.
        initial = initial + 1;
    } 
     
    
    return 0;
}
